# ── Docx styling constants ───────────────────────────────────────────────────

NAME_SIZE = Pt(14)
CONTACT_SIZE = Pt(10)
SECTION_HEADER_SIZE = Pt(10.5)
JOB_TITLE_SIZE = Pt(10)
BODY_SIZE = Pt(10)
SKILL_SIZE = Pt(10)

FONT_NAME = "Calibri"
SECTION_SPACING_BEFORE = Pt(6)
BULLET_SPACING = Pt(1)


# ── JSON extraction ─────────────────────────────────────────────────────────

def _extract_json(text: str) -> dict:
    """Extract JSON from LLM response, stripping markdown fences or preamble."""
    text = text.strip()
    # Strip markdown code fences
    text = re.sub(r"^```(?:json)?\s*\n?", "", text)
    text = re.sub(r"\n?```\s*$", "", text)
    text = text.strip()

    # Find the JSON object
    start = text.find("{")
    if start == -1:
        raise ValueError(f"No JSON object found. Response starts with: {text[:200]!r}")

    # Find matching closing brace
    depth = 0
    for i in range(start, len(text)):
        if text[i] == "{":
            depth += 1
        elif text[i] == "}":
            depth -= 1
            if depth == 0:
                json_str = text[start:i + 1]
                try:
                    return json.loads(json_str)
                except json.JSONDecodeError as e:
                    raise ValueError(
                        f"Invalid JSON: {e}. First 300 chars: {json_str[:300]!r}"
                    ) from e

    raise ValueError(f"Unterminated JSON object. First 300 chars: {text[:300]!r}")


# ── Docx helpers ─────────────────────────────────────────────────────────────

def _set_paragraph_spacing(paragraph, before=Pt(0), after=Pt(0)):
    pf = paragraph.paragraph_format
    pf.space_before = before
    pf.space_after = after


def _add_bottom_border(paragraph):
    pPr = paragraph._p.get_or_add_pPr()
    pBdr = OxmlElement("w:pBdr")
    bottom = OxmlElement("w:bottom")
    bottom.set(qn("w:val"), "single")
    bottom.set(qn("w:sz"), "4")
    bottom.set(qn("w:space"), "1")
    bottom.set(qn("w:color"), "000000")
    pBdr.append(bottom)
    pPr.append(pBdr)


def _add_run(paragraph, text, bold=False, italic=False, size=BODY_SIZE, font=FONT_NAME):
    run = paragraph.add_run(text)
    run.bold = bold
    run.italic = italic
    run.font.size = size
    run.font.name = font
    return run


def _add_section_header(doc, title):
    p = doc.add_paragraph()
    _set_paragraph_spacing(p, before=SECTION_SPACING_BEFORE, after=Pt(2))
    _add_run(p, title.upper(), bold=True, size=SECTION_HEADER_SIZE)
    _add_bottom_border(p)
    return p


def _add_bullet(doc, text, spacing_after=BULLET_SPACING):
    p = doc.add_paragraph(style="List Bullet")
    _set_paragraph_spacing(p, before=Pt(0), after=spacing_after)
    run = p.runs[0] if p.runs else p.add_run()
    run.text = ""
    _add_run(p, text, size=BODY_SIZE)
    return p


